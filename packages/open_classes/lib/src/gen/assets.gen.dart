class AssetGenImage {
  const AssetGenImage(this._assetName);

  final String _assetName;

  Image image({
    final Key? key,
    final AssetBundle? bundle,
    final ImageFrameBuilder? frameBuilder,
    final ImageErrorWidgetBuilder? errorBuilder,
    final String? semanticLabel,
    final bool excludeFromSemantics = false,
    final double? scale,
    final double? width,
    final double? height,
    final Color? color,
    final Animation<double>? opacity,
    final BlendMode? colorBlendMode,
    final BoxFit? fit,
    final AlignmentGeometry alignment = Alignment.center,
    final ImageRepeat repeat = ImageRepeat.noRepeat,
    final Rect? centerSlice,
    final bool matchTextDirection = false,
    final bool gaplessPlayback = false,
    final bool isAntiAlias = false,
    final String? package,
    final FilterQuality filterQuality = FilterQuality.low,
    final int? cacheWidth,
    final int? cacheHeight,
  }) {
    return Image.asset(
      _assetName,
      key: key,
      bundle: bundle,
      frameBuilder: frameBuilder,
      errorBuilder: errorBuilder,
      semanticLabel: semanticLabel,
      excludeFromSemantics: excludeFromSemantics,
      scale: scale,
      width: width,
      height: height,
      color: color,
      opacity: opacity,
      colorBlendMode: colorBlendMode,
      fit: fit,
      alignment: alignment,
      repeat: repeat,
      centerSlice: centerSlice,
      matchTextDirection: matchTextDirection,
      gaplessPlayback: gaplessPlayback,
      isAntiAlias: isAntiAlias,
      package: package,
      filterQuality: filterQuality,
      cacheWidth: cacheWidth,
      cacheHeight: cacheHeight,
    );
  }

  ImageProvider provider() => AssetImage(_assetName);

  String get path => _assetName;

  String get keyName => _assetName;
}
